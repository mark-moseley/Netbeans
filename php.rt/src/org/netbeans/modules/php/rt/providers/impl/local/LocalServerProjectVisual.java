    protected void updateDocumentPath(LocalHostImpl host) {
        try {

            String basePath = LocalHostImpl.Helper.getFileUrl(host);
            String path = basePath == null 
                    ? LocalHostImpl.Helper.noFileMessage()
                    : createPreviewPath(basePath);

            getDocumentPath().setText(path);
            getDocumentPath().setToolTipText(path);
        } catch (MalformedURLException ex) {
            getDocumentPath().setText(ex.getMessage());
        }
    }

    protected void updateHttpPath(LocalHostImpl host) {
        try {
            String baseUrl = LocalHostImpl.Helper.getHttpUrl(host);
            String httpUrl = baseUrl == null 
                    ? LocalHostImpl.Helper.noHttpMessage()
                    : createPreviewUrl(baseUrl);
            
            getHttpPath().setText(httpUrl);
            getHttpPath().setToolTipText(httpUrl);
        } catch (MalformedURLException ex) {
            getHttpPath().setText(ex.toString());
        }
    }

    private String createPreviewUrl(String baseUrl) throws MalformedURLException {
        String contextPath = getContextPath().getText();
        return LocalHostImpl.Helper.addSubdirectoryToUrl(baseUrl, contextPath);
    }

    private String createPreviewPath(String basePath) throws MalformedURLException {
        String contextPath = getContextPath().getText();
        return LocalHostImpl.Helper.addSubdirectoryToPath(basePath, contextPath);
    }



