    void load() {
        // load
        Boolean rewrite = ServerActionsPreferences.getInstance().getFileOverwrite();
        String rewriteMode = ASK_ME;
        if (rewrite != null){
            rewriteMode = rewrite.booleanValue() ? YES : NO;
        }
        myRewriteFileCombo.setSelectedItem(rewriteMode);
    }

    void store() {
        Boolean rewrite = null;
        String selectedMode = (String)myRewriteFileCombo.getSelectedItem();
        if (selectedMode != null){
            if (selectedMode.equals(YES)){
                rewrite = true;
            } else if (selectedMode.equals(NO)){
                rewrite = false;
            }
        }
        ServerActionsPreferences.getInstance().setFileOverwrite(rewrite);
    }

