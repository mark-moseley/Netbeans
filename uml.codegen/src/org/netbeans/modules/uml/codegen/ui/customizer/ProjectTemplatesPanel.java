    private TemplateModel model = new TemplateModel(); 
    private VerticalTabbedPanel templateFamilies = null;
    
    /** Creates new form NewJPanel */
    public ProjectTemplatesPanel(UMLProjectProperties umlPrjProps)
    {
        initComponents();
        model.setUMLProjectProperties(umlPrjProps);
        templateFamilies = new VerticalTabbedPanel(model, TabbedPanel.EXPAND_SOME);
        tabbedPaneContainer.setViewportView(templateFamilies);
    }
    
    
    public void actionPerformed(ActionEvent event)
    {
        String cmd = event.getActionCommand();
        
        // listener of the OK button in the Project Customizer
        if (cmd.equals("OK")) // NOI18N
        {
            // Project Customizer UI
            if (model != null && model.getUMLProjectProperties() != null)
            {
                model.getUMLProjectProperties().save();
            }
        }
    }
    
