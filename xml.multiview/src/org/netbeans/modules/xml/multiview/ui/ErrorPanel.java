    public void writeMessage(String message, int type){
        switch (type) {
            case ERROR_MESSAGE: {
                setForeground(UIManager.getDefaults().getColor("ToolBar.dockingForeground"));
            }
            break;
            case STATUS_MESSAGE: {
                setForeground(UIManager.getDefaults().getColor("Table.selectionBackground"));
            }
            break;
            case BLANK_MESSAGE: {
                setText("");
            }
            return;
        }        
        setText(message);
        
    }
   
        
