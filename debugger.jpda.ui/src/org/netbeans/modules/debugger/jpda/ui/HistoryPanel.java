    private class Item {

        String expr;
        String type;
        String value;

        Item(String expr, String type, String value) {
            this.expr = expr;
            this.type = type;
            this.value = value;
        }

        Vector toVector() {
            Vector result = new Vector();
            result.add(expr);
            result.add(type);
            result.add(value);
            return result;
        }

    }

    class HistoryTableModel extends DefaultTableModel {

        private static final int MAX_ITEMS = 20;

        private ArrayList<Item> historyItems = new ArrayList<Item>();

        private void addItem(String expr, String type, String value) {
            Item item = new Item(expr, type, value);
            historyItems.add(0, item);
            if (historyItems.size() > MAX_ITEMS) {
                historyItems.remove(MAX_ITEMS);
            }
            fireTableDataChanged();
        }

        public void clearHistory() {
            historyItems.clear();
            fireTableDataChanged();
        }

        @Override
        public int getRowCount() {
            if (historyItems == null) {
                historyItems = new ArrayList<Item>();
            }
            return historyItems.size();
        }

        @Override
        public int getColumnCount() {
            return 3;
        }

        @Override
        public String getColumnName(int columnIndex) {
            String id = null;
            switch (columnIndex) {
                case 0: id = "CTL_Expression"; break;
                case 1: id = "CTL_Type"; break;
                case 2: id = "CTL_Value"; break;
            }
            return NbBundle.getMessage (HistoryPanel.class, id);
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            if (rowIndex >= historyItems.size()) {
                return "<???>"; // NOI18N
            }
            Item item = historyItems.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return item.expr;
                case 1:
                    return item.type;
                case 2:
                    return item.value;
            }
            return "<???>"; // NOI18N
        }

    }

