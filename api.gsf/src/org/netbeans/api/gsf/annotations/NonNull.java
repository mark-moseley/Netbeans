package org.netbeans.api.gsf.annotations;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * The annotation target should never be null. This means that it is an error to set it to
 * null (in the case of fields or variables), or pass in null (in the case of parameters), or
 * return null (in the case of methods).  It is also unnecessary to check whether these
 * targets are null in client code.
 */
@Documented
@Target({ElementType.FIELD, ElementType.METHOD, ElementType.PARAMETER, ElementType.LOCAL_VARIABLE})
@Retention(RetentionPolicy.CLASS)
public @interface NonNull {
}
