package org.netbeans.modules.uml.core.metamodel.core.primitivetypes;

/**
 * @author Aztec
 */
public interface IPartKind
{
    // A Classifier that defines the core logic or control flow for one or more
    // auxiliary Classifiers that support it.
    public static final int PK_FOCUS = 0;

    // A Classifier that supports another more central or fundamental Classifier
    // within a Component, typically by implementing secondary logic or control
    // flow.
    public static final int PK_AUXILIARY = 1;

    // A Classifier that sits behind a component Interface and realizes that 
    // interface.
    public static final int PK_INTERFACEIMPLEMENTATION = 2;
}
