package org.netbeans.lexer.gen;

import java.io.File;
import java.io.IOException;

import org.apache.tools.ant.BuildException;
import org.apache.tools.ant.Task;

import org.netbeans.modules.lexer.gen.javacc.JavaCCLanguageDescriptionGenerator;

/**
 * Generate language description xml file from xxxTokenTypes class
 * generated by Antlr.
 * <BR>If the file already exists the content is appended to the end
 * of the file.
 *
 * @author Miloslav Metelka
 */
public class JavaCCGenerateLanguageDescription extends GenerateLanguageDescription {
    
    protected String generate(String tokenTypesClassName) throws ClassNotFoundException {
        
        JavaCCLanguageDescriptionGenerator generator
            = new JavaCCLanguageDescriptionGenerator();

        return generator.generate(tokenTypesClassName);
    }
            
}
