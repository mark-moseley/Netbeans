    private static final String DELETE_BASE = "/org/netbeans/modules/hudson/ui/resources/document_delete.gif";
    
    /** Creates new form HudsonJobChangePanel */
    public HudsonJobChangePanel(HudsonJobChangeItem change) {
        initComponents();
        
        userLabel.setText(NbBundle.getMessage(HudsonJobChangePanel.class, "LBL_User", change.getUser()));
        msgLabel.setText(NbBundle.getMessage(HudsonJobChangePanel.class, "LBL_Msg", change.getMsg()));
        
        GridBagConstraints gbc = new java.awt.GridBagConstraints();
        gbc.gridx = 0;
        gbc.gridy = 0;
        gbc.fill = java.awt.GridBagConstraints.HORIZONTAL;
        gbc.weightx = 1.0;
        gbc.insets = new java.awt.Insets(0, 0, 5, 0);
        
        for (HudsonJobChangeFile file : change.getFiles()) {
            ImageIcon icon = null;
            
            switch(file.getEditType()) {
            case add:
                icon = new ImageIcon(getClass().getResource(ADD_BASE));
                break;
            case delete:
                icon = new ImageIcon(getClass().getResource(DELETE_BASE));
                break;
            case edit:
                icon = new ImageIcon(getClass().getResource(EDIT_BASE));
                break;
            default:
                icon = new ImageIcon(getClass().getResource(EDIT_BASE));
                break;
            }
            
            filesPanel.add(new JLabel(NbBundle.getMessage(HudsonJobChangePanel.class,
                    "LBL_File", file.getName(), file.getRevision()), icon, JLabel.LEFT), gbc);
            
            gbc.gridy++;
        }
    }
    
